<template>
  <div id="app">
    <router-view />
  </div>
</template>

<script setup>
import { onMounted } from 'vue'
import { useUserStore } from '@/store/user'

const userStore = useUserStore()

onMounted(() => {
  // 应用初始化时检查登录状态
  const token = localStorage.getItem('token')
  if (token) {
    userStore.initializeAuth(token)
  }
})
</script>

